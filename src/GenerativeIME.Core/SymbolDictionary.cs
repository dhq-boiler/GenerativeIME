    private static void AddRow(Dictionary<char, char> d, string row, string? vowels = null)
    {
        var v = vowels ?? "あいうえお";
        for (var i = 0; i < row.Length && i < v.Length; i++)
        {
            // 母音は ひらがな に正規化（カタカナ行でも ひらがな の母音を返す）
            var vv = v[i];
            if (vv >= 'ア' && vv <= 'オ')
            {
                vv = (char)(vv - 0x60);
            }

            d[row[i]] = vv;
        }
    }

    private static char VowelOf(char hira)
    {
        return Vowels.TryGetValue(hira, out var v) ? v : '\0';
    }
